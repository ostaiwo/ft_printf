#include "libft.h"

int	ft_atoi(const char *str)
{
	int	ret;
	int	i;
	int	sign;

	i = 0;
	ret = 0;
	sign = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (str[i] >= '0' && str[i] <= '9')
			ret = (ret * 10) + (str[i] - '0');
		i++;
	}
	return (ret * sign);
}
